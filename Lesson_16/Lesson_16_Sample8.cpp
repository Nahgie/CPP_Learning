#include <iostream>

int main() {

	std::cout << "10을 10진수로 표현하면" << std::dec << 10 << "입니다." << std::endl;
	std::cout << "10을 8진수로 표현하면" << std::oct << 10 << "입니다." << std::endl;
	std::cout << "12을 8진수로 표현하면" << 12 << "입니다." << std::endl;
	std::cout << "10을 16진수로 표현하면" << std::hex << 10 << "입니다." << std::endl;


	return 0;
}